package langaugeBasics;

public class Variables
{
	public static void main( String[] args )
	{
		byte byteDefault = 0;
		short shortDefault = 0;
		int intDefault = 0;
		long longDefault = 0L;
		float floatDefault = 0.0f;
		double doubleDefault = 0.0d;
		char charDefault = '\u0000';
		String stringDefault = null;
		boolean booleanDefault = false;
		
		System.out.println( "byteDefault is: " + byteDefault );
		System.out.println( "shortDefault is: " + shortDefault );
		System.out.println( "intDefault is: " + intDefault );
		System.out.println( "longDefault is: " + longDefault );
		System.out.println( "floatDefault is: " + floatDefault );
		System.out.println( "doubleDefault is: " + doubleDefault );
		System.out.println( "charDefault is: " + charDefault );
		System.out.println( "stringDefault is: " + stringDefault );
		System.out.println( "booleanDefault is: " + booleanDefault );
	}
}
